package viewModel;

/**
 *
 * @author FPTSHOP
 */
public class ViewItem {

    private String name;
    private String amount;
    private String status;

    public ViewItem() {
    }

    public ViewItem(String name, String amount, String status) {
        this.name = name;
        this.amount = amount;
        this.status = status;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

}
