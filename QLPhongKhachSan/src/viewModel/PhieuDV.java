
package viewModel;


public class PhieuDV {
    String idHD;
    String maHD;
    String sophong;
    String tenDV;
    String maDV;
    String ngaysudung;
    String solan;
    String gia;
    String giamgia;
    String thanhtien;
    public PhieuDV() {
    }

    public PhieuDV(String idHD, String maHD, String sophong, String tenDV, String maDV, String ngaysudung, String solan, String gia, String giamgia, String thanhtien) {
        this.idHD = idHD;
        this.maHD = maHD;
        this.sophong = sophong;
        this.tenDV = tenDV;
        this.maDV = maDV;
        this.ngaysudung = ngaysudung;
        this.solan = solan;
        this.gia = gia;
        this.giamgia = giamgia;
        this.thanhtien = thanhtien;
    }

    public String getIdHD() {
        return idHD;
    }

    public void setIdHD(String idHD) {
        this.idHD = idHD;
    }

    public String getMaHD() {
        return maHD;
    }

    public void setMaHD(String maHD) {
        this.maHD = maHD;
    }

    public String getSophong() {
        return sophong;
    }

    public void setSophong(String sophong) {
        this.sophong = sophong;
    }

    public String getTenDV() {
        return tenDV;
    }

    public void setTenDV(String tenDV) {
        this.tenDV = tenDV;
    }

    public String getMaDV() {
        return maDV;
    }

    public void setMaDV(String maDV) {
        this.maDV = maDV;
    }

    public String getNgaysudung() {
        return ngaysudung;
    }

    public void setNgaysudung(String ngaysudung) {
        this.ngaysudung = ngaysudung;
    }

    public String getSolan() {
        return solan;
    }

    public void setSolan(String solan) {
        this.solan = solan;
    }

    public String getGia() {
        return gia;
    }

    public void setGia(String gia) {
        this.gia = gia;
    }

    public String getGiamgia() {
        return giamgia;
    }

    public void setGiamgia(String giamgia) {
        this.giamgia = giamgia;
    }

    public String getThanhtien() {
        return thanhtien;
    }

    public void setThanhtien(String thanhtien) {
        this.thanhtien = thanhtien;
    }

   
}
